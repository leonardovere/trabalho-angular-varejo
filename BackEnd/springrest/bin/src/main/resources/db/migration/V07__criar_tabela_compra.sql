CREATE TABLE compra(
	codigo BIGINT(20) PRIMARY KEY AUTO_INCREMENT,
	COMPRA_CARRINHO BIGINT(20) NOT NULL,
	DESCONTO FLOAT,
	VALORCOMDESCONTO FLOAT,
	DT_COMPRA TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


