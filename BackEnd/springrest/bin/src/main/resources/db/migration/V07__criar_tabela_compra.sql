	COD_COMPRA BIGINT,
	NUMCARTAO BIGINT,
	NUMBOLETO BIGINT,
	AGENCIA INT,
	VEZES INT,
	DT_EMISSAO DATE,
	DT_VENCIMENTO DATE

)ENGINE=InnoDB DEFAULT CHARSET=utf8;

